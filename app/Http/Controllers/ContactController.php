     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {

        $request->validate([
            'name' => 'required',
            'email' => 'required',
            'phone' => 'required',
            'message' => 'required',
        ]);


        $input = $request->all();

        Contact::create($input);

        return redirect()->back()->with('success' , 'Contact Form Submit Successfully');
    }

